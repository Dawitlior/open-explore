import { ReactNode } from 'react';
import type { TradingTheme } from '@/lib/trading-theme';
import { GlassCard } from './TradingUI';

export interface ChartExplanation {
  what: string;
  why: string;
  interpret: string;
  good: string;
  action: string;
}

interface ChartWrapperProps {
  T: TradingTheme;
  title: string;
  explanation: ChartExplanation;
  children: ReactNode;
  style?: React.CSSProperties;
  unit?: string;
  chartId?: string;
  onRemove?: (chartId: string) => void;
  onExplainClick?: (title: string, explanation: ChartExplanation, chartId?: string) => void;
}

export const ChartWrapper = ({ T, title, explanation, children, style, unit, chartId, onRemove, onExplainClick }: ChartWrapperProps) => {
  const handleInfoClick = () => {
    if (onExplainClick) {
      onExplainClick(title, explanation, chartId);
    }
  };

  return (
    <GlassCard T={T} style={{ position: 'relative', ...style }}>
      <div style={{ display: 'flex', alignItems: 'center', justifyContent: 'space-between', marginBottom: 10 }}>
        <div
          style={{ display: 'flex', alignItems: 'center', gap: 6, cursor: 'pointer' }}
          onClick={handleInfoClick}
        >
          <div style={{ fontSize: 10, color: T.text.muted, textTransform: 'uppercase', letterSpacing: '0.08em' }}>{title}</div>
          {unit && <span style={{ fontSize: 8, padding: '1px 5px', borderRadius: 4, background: `${T.accent.purple}15`, color: T.accent.purple, fontWeight: 600 }}>{unit}</span>}
        </div>
        <button
          onClick={handleInfoClick}
          style={{
            width: 18, height: 18, borderRadius: '50%', border: `1px solid ${T.border.medium}`,
            background: 'transparent', color: T.text.muted,
            cursor: 'pointer', fontSize: 10, fontWeight: 700, display: 'flex', alignItems: 'center', justifyContent: 'center',
            transition: 'all 0.2s'
          }}
        >
          i
        </button>
      </div>
      <ChartErrorBoundary T={T}>{children}</ChartErrorBoundary>
    </GlassCard>
  );
};

